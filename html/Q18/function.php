<?php
//足し算
function calc1(int $n1, int $n2): int
{
    return $n1 + $n2;
}
//引き算
function calc2(int $n1, int $n2): int
{
    return $n1 - $n2;
}
//掛け算
function calc3(int $n1, int $n2): int
{
    return $n1 * $n2;
}
//割り算
function calc4(int $n1, int $n2): float
{
    return $n1 / $n2;
}
